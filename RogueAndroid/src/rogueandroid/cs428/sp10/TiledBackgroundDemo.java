    // public FixedBackground background;
    // public TiledBackground background;
    public ScrollingTiledBackground background;

    /**
     * onCreate is called with the activity is created. It loads the Rokon game
     * engine.
     * 
     * @param none
     * @return none
     */
    public void onCreate() {
        createEngine(480, 320, true);
    }

    /**
     * onLoad is called after the game engine has been created, it initializes
     * the graphics elements within the engine
     * 
     * @param none
     * @return none
     */
    @Override
    public void onLoad() {

        // background = new FixedBackground(backgroundTexture);
        tiledTextures[0] = new Texture("graphics/sprites/se_free_grass_texture.bmp");
        tiledTextures[1] = new Texture("graphics/sprites/se_free_road_texture.bmp");
        tiledTextures[2] = new Texture("graphics/sprites/se_free_dirt_texture.bmp");
        background = new ScrollingTiledBackground(tiledTextures, tiles, 96.0f, 96.0f);
    }

    /**
     * onLoadComplete once the graphic elements have been loaded into the Rokon
     * engine, display the background
     * 
     * @param none
     * @return none
     */
    @Override
    public void onLoadComplete() {
        rokon.setBackground(background);
    }

    /**
     * onGameLoop the main game processing loop. Scroll the background a fixed
     * amount to demonstrate scrolling the tiles.
     * 
     * @param none
     * @return none
     */
    @Override
    public void onGameLoop() {
        background.setScroll(background.getScrollX() - 5.0f, background.getScrollY() - 5.0f);
    }

    /**
     * onRestart if the activity is paused, perhaps due to a phone call or other
     * phone event, this method is called to notify the application that it is
     * active again.
     * 
     * @param none
     * @return none
     */
    @Override
    public void onRestart() {
        super.onRestart();
        rokon.unpause();
    }
